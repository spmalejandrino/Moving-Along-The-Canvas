
public class Loop
{

    public Loop()
    {
    }

    public void print(int x)
    {
        int i = x;
        while (i <= 10)
         {
            System.out.println(i);
            i++;
        }
        int z = x;
        while (z >= 10)
        {
            System.out.println(z);
            z--;
        }
    }
}
